//counting
for(var i=0; i<=15; i++){
    document.write(i+" ");
}

//reverse-counting
for(var i=10; i>=1; i--){
    document.write(i+" ");
}

//even
for(var i=0; i<=20; i=i+2){
    document.write(i+" ")
}

//odd
for(var i=1; i<=20; i++){
    if(i%2!==0){
        document.write(i+" ");
    }
}
//series
for(var i=0; i<=20; i++){
    if(i%2==0){
        document.write(i+"k"+" ");
    }
}